@section('scripts')
    <script>
        /*Para que se adapte bien con el autofocus*/
        if($('#form-login input').is(':focus')){
            $('#iconuser').addClass('g-brd-primary');
        }
        /*Para que se colore el borde de los iconos*/
        $(function() {
            $('#mostrarcontraseña').click(function () {
                if('password' == $('#password').attr('type')){
                    $('#password').prop('type', 'text');
                }else{
                    $('#password').prop('type', 'password');
                }
                $(this).children().toggleClass('fa-eye-slash fa-eye')
            });
            $('#form-login input').focus(function () {
                $(this).siblings().children().addClass('g-brd-primary');
            });
            $('#form-login input').focusout(function () {
                $(this).siblings().children().removeClass('g-brd-primary');
            });
        });
    </script>
@endsection